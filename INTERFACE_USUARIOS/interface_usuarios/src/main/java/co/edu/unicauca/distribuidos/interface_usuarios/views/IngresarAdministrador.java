    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        
        try {
            UsuarioServices objAdminServices = new UsuarioServices();
            AdminDTO objAdmin = new AdminDTO(jTextFieldNombre.getText(), jTextFieldApellido.getText(), jTextFieldLogin.getText(), jTextFieldContrasenia.getText());
            AdminDTO objAdminRegistrado = objAdminServices.registrarAdmin(objAdmin);
            JOptionPane.showMessageDialog(null, "ADMINISTRADOR REGISTRADO CON EXITO!!!");
            Login ventanaLogin = new Login();
            this.setVisible(false);
            ventanaLogin.setVisible(true);
        } catch (Exception NullException) {
            JOptionPane.showMessageDialog(null, "ADMINISTRADOR NO REGISTRADO!!!","ERROR_MESSAGE", JOptionPane.ERROR_MESSAGE);
            Login ventanaLogin = new Login();
            this.setVisible(false);
            ventanaLogin.setVisible(true);
        }
        jTextFieldApellido.setText("");
        jTextFieldContrasenia.setText("");
        jTextFieldLogin.setText("");
        jTextFieldNombre.setText("");
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

